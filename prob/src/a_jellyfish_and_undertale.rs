fn main() {
    let mut sc = Scanner::default();
    let out = &mut BufWriter::new(stdout());

    let t = sc.next();
    for _ in 1..=t {
        let a = sc.next::<i64>();
        let b = sc.next::<i64>();
        let mut n = sc.next::<i64>();

        let ans: i64 = (0..n).map(|_| (a - 1).min(sc.next::<i64>())).sum();

        writeln!(out, "{}", ans + b).unwrap()
    }
}
