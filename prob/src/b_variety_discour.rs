fn solve() {
    let n = rin!(usize);
    let mut a = rin_vec!(i32);
    let set: HashSet<i32> = a.iter().cloned().collect();
    a.sort_unstable();

    let mut ans1 = 0;
    if n > 1 {
        ans1 = a[n - 1] - a[n - 2];
    } else {
        println!("{} {}", a[0], a[0]);
        return;
    }

    let mut ans2 = (n - set.len()) as i32;

    if ans1 >= ans2 && set.len() != 1 {
        println!("{} {}", a[n - 2], a[n - 1]);
    } else if set.len() == 1 {
        routln!(0)
    } else {
        routln!(ans2)
    }
}
